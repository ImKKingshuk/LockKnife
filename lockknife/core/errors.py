"""Structured error codes for LockKnife.

Error codes follow the pattern LK-XXXX where XXXX is a 4-digit number:
- LK-0001 to LK-0999: Core/config errors
- LK-1000 to LK-1999: Device/ADB errors
- LK-2000 to LK-2999: Extraction errors
- LK-3000 to LK-3999: Forensics errors
- LK-4000 to LK-4999: Exploitation errors
- LK-5000 to LK-5999: Runtime instrumentation errors
- LK-6000 to LK-6999: Intelligence errors
- LK-7000 to LK-7999: Reporting errors
"""

from enum import Enum


class LockKnifeErrorCode(str, Enum):
    """Structured error codes for LockKnife exceptions."""

    # Core/Config errors (LK-0001 to LK-0999)
    CORE_CONFIG_INVALID = "LK-0001"
    CORE_CONFIG_MISSING = "LK-0002"
    CORE_CONFIG_PARSE_ERROR = "LK-0003"
    CORE_SECRET_MISSING = "LK-0004"
    CORE_EXTERNAL_TOOL_MISSING = "LK-0005"

    # Device/ADB errors (LK-1000 to LK-1999)
    DEVICE_ADB_TIMEOUT = "LK-1001"
    DEVICE_ADB_CONNECTION_FAILED = "LK-1002"
    DEVICE_NOT_FOUND = "LK-1003"
    DEVICE_OFFLINE = "LK-1004"
    DEVICE_AUTH_FAILED = "LK-1005"
    DEVICE_SHELL_FAILED = "LK-1006"
    DEVICE_PULL_FAILED = "LK-1007"
    DEVICE_PUSH_FAILED = "LK-1008"

    # Extraction errors (LK-2000 to LK-2999)
    EXTRACTION_SMS_FAILED = "LK-2001"
    EXTRACTION_CONTACTS_FAILED = "LK-2002"
    EXTRACTION_CALL_LOGS_FAILED = "LK-2003"
    EXTRACTION_BROWSER_FAILED = "LK-2004"
    EXTRACTION_LOCATION_FAILED = "LK-2005"
    EXTRACTION_MEDIA_FAILED = "LK-2006"
    EXTRACTION_WIFI_FAILED = "LK-2007"

    # Forensics errors (LK-3000 to LK-3999)
    FORENSICS_SNAPSHOT_FAILED = "LK-3001"
    FORENSICS_TIMELINE_FAILED = "LK-3002"
    FORENSICS_CORRELATION_FAILED = "LK-3003"
    FORENSICS_SQLITE_ANALYSIS_FAILED = "LK-3004"
    FORENSICS_ARTIFACT_PARSE_FAILED = "LK-3005"

    # Exploitation errors (LK-4000 to LK-4999)
    EXPLOIT_AUTH_DENIED = "LK-4001"
    EXPLOIT_AUTH_INVALID_SCOPE = "LK-4002"
    EXPLOIT_SCAN_FAILED = "LK-4003"
    EXPLOIT_CONNECT_FAILED = "LK-4004"
    EXPLOIT_EXECUTE_FAILED = "LK-4005"
    EXPLOIT_LAB_MODE_REQUIRED = "LK-4006"

    # Runtime instrumentation errors (LK-5000 to LK-5999)
    RUNTIME_FRIDA_ATTACH_FAILED = "LK-5001"
    RUNTIME_FRIDA_SCRIPT_FAILED = "LK-5002"
    RUNTIME_HOOK_FAILED = "LK-5003"
    RUNTIME_MEMORY_DUMP_FAILED = "LK-5004"
    RUNTIME_TRACE_FAILED = "LK-5005"

    # Intelligence errors (LK-6000 to LK-6999)
    INTEL_CVE_LOOKUP_FAILED = "LK-6001"
    INTEL_OSV_LOOKUP_FAILED = "LK-6002"
    INTEL_FINGERPRINT_FAILED = "LK-6003"
    INTEL_CHAIN_BUILD_FAILED = "LK-6004"
    INTEL_API_KEY_MISSING = "LK-6005"

    # Reporting errors (LK-7000 to LK-7999)
    REPORT_TEMPLATE_MISSING = "LK-7001"
    REPORT_TEMPLATE_PARSE_FAILED = "LK-7002"
    REPORT_RENDER_FAILED = "LK-7003"
    REPORT_OUTPUT_FAILED = "LK-7004"
    REPORT_PDF_BACKEND_MISSING = "LK-7005"
